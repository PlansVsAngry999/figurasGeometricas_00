    private void btnCuadradoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCuadradoMouseClicked
            //Limpiar label's de resultado.
       lblPerimetro.setText(null);
       lblArea.setText(null);
            //Declarar datos.
       double area, perimetro, lado;
            //Solicitar datos.
       lado = Double.parseDouble(JOptionPane.showInputDialog(null,"Ingresa el valor del lado..."));
             //Area y perimetro del cuadrado.
        area = lado * lado;
        perimetro = 4*lado;
            //Muestra los datos al usuario
        lblPerimetro.setText("Perimetro = " + perimetro + "\n");
        lblArea.setText("Area = " + area + "\n");
        lblTitulo2.setText("RESULTADOS..."); //Toque estético
    }//GEN-LAST:event_btnCuadradoMouseClicked

    private void btnCirculoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnCirculoMouseClicked
            //Limpiar label's de resultado.
       lblPerimetro.setText(null);
       lblArea.setText(null);
            //Declarar datos.
       double radio, perimetro, area;
       double pi = 3.14159265358979323846;
            // Solicitar datos..
       radio = Double.parseDouble(JOptionPane.showInputDialog(null,"Ingresa el valor del radio..."));
            //Area y perimetro del circulo.
       perimetro = 2 * (pi * radio);
       area = pi * (radio * radio);
            //Muestra los datos al usuario
       lblPerimetro.setText("Perimetro = " + perimetro + "\n");
       lblArea.setText("Area = " + area + "\n");
       lblTitulo2.setText("RESULTADOS..."); //Toque estético
    }//GEN-LAST:event_btnCirculoMouseClicked

