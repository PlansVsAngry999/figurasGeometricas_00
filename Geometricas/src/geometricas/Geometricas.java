    private void btnRectanguloMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnRectanguloMouseClicked
             //Limpiar label's de resultado.
       lblPerimetro.setText(null);
       lblArea.setText(null);
             //Declarar datos.
       double area, perimetro, base, altura;
            //Solicitar datos.
       base = Double.parseDouble(JOptionPane.showInputDialog(null,"Ingresa el valor de la base..."));
       altura = Double.parseDouble(JOptionPane.showInputDialog(null,"Ingresa el valor de la altura..."));
            //Area y perimetro del rectángulo.
        area = base * altura;
        perimetro = (2 * base) + (2 * altura);
             //Muestra los datos al usuario
        lblPerimetro.setText("Perimetro = " + perimetro + "\n");
        lblArea.setText("Area = " + area + "\n");
        lblTitulo2.setText("RESULTADOS..."); //Toque estético
    }//GEN-LAST:event_btnRectanguloMouseClicked
